"""Standard widget sizes for consistent UI appearance."""

# Standard widget height in pixels
# This ensures entries and buttons have consistent heights
STANDARD_WIDGET_HEIGHT = 28

# Standard padding for entries to match button height
# Entry height is controlled by font size and padding
ENTRY_PADY = 4  # Vertical padding for entries

# Standard button padding
BUTTON_PADY = 6  # Vertical padding for buttons (combined with font gives STANDARD_WIDGET_HEIGHT)
